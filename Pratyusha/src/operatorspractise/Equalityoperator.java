package operatorspractise;

/*
Equality or relational operators -> Boolean operatoer
*/
public class Equalityoperator {
    public static void main(String[] args) {
        int num1 = 30, num2 = 40;
        System.out.println(num1>num2);
        System.out.println(num1<num2);
        System.out.println(num1>=num2);
        System.out.println(num1<=num2);
        System.out.println(num1==num2);
        System.out.println(num1!=num2);
    }
}
