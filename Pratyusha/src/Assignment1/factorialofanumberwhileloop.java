package Assignment1;

public class factorialofanumberwhileloop {
    public static void main(String[] args) {

        int input = 5;
        int i = 1;
        int j = 1;
        while(i<=input)
        {
           j=j*i;
           i++; 
            
        }
        {
            System.out.println(j);
        }
    }
}
