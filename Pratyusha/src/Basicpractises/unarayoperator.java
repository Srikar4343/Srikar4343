package Basicpractises;

/*
Unary Operators
+  plus indicates positive value;
-  minus indicates negative expression;
++  Increment operator(Increments a value by 1);
--  Decrement operator(Decrement a value by 1);
!   Logical compliment(gives the value of a boolean)
*/
public class unarayoperator {
    public static void main(String[] args) {
        int i = 20;
        System.out.println(+i);
        System.out.println(-i);
        System.out.println(++i);
        System.out.println(--i);
        System.out.println(!true);
        System.out.println(i++);
        System.out.println(i--);
        System.out.println(--i);
    }
}
