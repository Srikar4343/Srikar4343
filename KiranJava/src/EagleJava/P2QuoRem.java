package EagleJava;
public class P2QuoRem {
    public static void main(String[] args) {

        int a = 26, b = 3;
    
        int quotient = a / b;
        int remainder = a % b;
    
        System.out.println("Quotient = " + quotient);
        System.out.println("Remainder = " + remainder);
      }
}
