package Assignments8;

public class Pr43 {
    public static void main(String[] args)
    {
 
        String string = "Characters";
 
        
        System.out.println(
            "Individual characters from given string: ");
 
        for (int i = 0; i < string.length(); i++) {
            System.out.print(string.charAt(i) + " ");
        }
    }
}
