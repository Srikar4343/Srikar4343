package Basicpractice;

public class Forloopdemo {
    public static void main(String[] args) {
        // For loop
        for(int i=1;i<=10;i++) // for(initialization;condition;incr/decr)
        {
            System.out.println(i);
        }

        // For Loop to print in reverse order

        for(int i=10;i>=1;i--) 
        {
            System.out.println(i);
        }   
        
    }
}
