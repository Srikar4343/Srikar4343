// Multiplication table of 5
public class Multilplication {
public static void main(String[] args) {
    int N = 5, range = 10, i = 1;
    while(i <= range){
    System.out.println(N + " * " + i + " = "  + N * i); // N*i=N*i => 5*1=5*1 = 5, then it increments i value when it gets into i++
    i++;
    }
}
}

    


